function p(
  partial: Omit<Product, "published" | "createdAt" | "images" | "specs"> & {
    images?: string[];
    specs?: Record<string, string>;
  }
): Product {
  const detectionRangeM =
    partial.detectionRangeM != null && partial.detectionRangeM > 0
      ? partial.detectionRangeM
      : estimateDetectionRangeM({
          resolution: partial.resolution,
          deviceType: partial.deviceType,
          categorySlug: partial.categorySlug,
          id: partial.id,
        });

  const baseSpecs = partial.specs ?? {
    "Матриця": partial.resolution || "—",
    "Тип":
      partial.deviceType === "scope"
        ? "Приціл"
        : partial.deviceType === "binocular"
          ? "Бінокль"
          : "Монокуляр",
    "NETD": "≤ 35 мК",
    "Частота": "50 Гц",
    "Захист": "IP67",
  };

  return {
    ...partial,
    detectionRangeM,
    images: partial.images ?? [],
    specs: {
      ...baseSpecs,
      "Дальність виявлення людини, м": String(detectionRangeM),
    },
    published: true,
    createdAt: partial.isNew
      ? new Date().toISOString()
      : "2025-11-01T10:00:00.000Z",
  };
}

