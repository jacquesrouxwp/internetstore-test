export function answerUserQuestion(
  text: string,
  faq: FaqItem[],
  locale: "uk" | "ru"
): string {
  const q = text.toLowerCase().trim();
  if (!q) {
    return locale === "ru"
      ? "Напишите вопрос или выберите один из быстрых вариантов ниже."
      : "Напишіть питання або оберіть один зі швидких варіантів нижче.";
  }

  let best: FaqItem | null = null;
  let bestScore = 0;
  for (const item of faq) {
    let score = 0;
    for (const kw of item.keywords) {
      if (q.includes(kw.toLowerCase())) score += 2;
    }
    // also match question words loosely
    const words = item.q.toLowerCase().split(/\s+/).filter((w) => w.length > 3);
    for (const w of words) {
      if (q.includes(w)) score += 1;
    }
    if (score > bestScore) {
      bestScore = score;
      best = item;
    }
  }

  if (best && bestScore >= 2) {
    return best.a;
  }

  return locale === "ru"
    ? "Пока не нашёл точный ответ в базе. Выберите быстрый вопрос ниже или напишите менеджеру в Telegram / Viber / WhatsApp — подберут прибор под задачу."
    : "Поки не знайшов точної відповіді в базі. Оберіть швидке питання нижче або напишіть менеджеру в Telegram / Viber / WhatsApp — підберуть прилад під задачу.";
}
